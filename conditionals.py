n = int(input("Enter a number, please - "))

print("n = " + str(n))

if n <= 0:
    print("n <= 0")
elif n >= 10:
    print("n >= 10")
elif n >= 5:
    print("n >= 5")
elif n >= 3:
    print("n >=3")
elif n == 1:
    print("n ==1")
elif n == -1:
    print("n == -1")
else:
    print("n equals something different ")